    private void guardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardarMouseClicked
        String numeroCamisaJugador = numeroCamisaTF.getText();
        String alturaJugador = alturaTF.getText();
        String nombreJugador = nombreTF.getText();
        String pesoJugador = pesoTF.getText();

        numeroCamisaTF.requestFocus();
        


        
        //Validar que no son vacios
        if (numeroCamisaJugador.isEmpty() || alturaJugador.isEmpty() || nombreJugador.isEmpty() || pesoJugador.isEmpty() || ( !numeroCamisaJugador.matches("[0-9]+") || numeroCamisaJugador == null ) || ( !alturaJugador.matches("[0-9]+") || alturaJugador == null ) || ( !pesoJugador.matches("[0-9]+") || pesoJugador == null )) {
            msgError.setVisible(true);
            contadorTiempo.schedule(contadorTarea, 2000);
        } else {
            int iteracionArreglo = Integer.parseInt(numeroJugador.getText());
            iteracionArreglo -= 1;

            String[] jugadoresEstadisticas = {numeroCamisaJugador, nombreJugador, alturaJugador, pesoJugador, "0", "0", "0", "0", "0"};
            informacionJugadores[iteracionArreglo] = jugadoresEstadisticas;

            int iteracionNumero = Integer.parseInt(numeroJugador.getText());
            iteracionNumero += 1;

            if (iteracionNumero < 13) {
                String iteracionNumeroLabel = Integer.toString(iteracionNumero);
                numeroJugador.setText(iteracionNumeroLabel);
            }

            numeroCamisaTF.setText(null);
            nombreTF.setText(null);
            alturaTF.setText(null);
            pesoTF.setText(null);

            if (iteracionArreglo == 11) {
                guardar.setVisible(false);
                MenuPersonal personalEquipo = new MenuPersonal();
                personalEquipo.setDataEquipo(informacionJugadores);
                personalEquipo.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_guardarMouseClicked

